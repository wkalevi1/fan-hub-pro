// Helper functions for localStorage simulation
export const getStoredVotes = () => {
  const stored = localStorage.getItem('fanhub_votes');
  return stored ? JSON.parse(stored) : {};
};

export const storeVote = (outfitId) => {
  const votes = getStoredVotes();
  votes[outfitId] = true;
  localStorage.setItem('fanhub_votes', JSON.stringify(votes));
};

export const getStoredQuestions = () => {
  const stored = localStorage.getItem('fanhub_questions');
  return stored ? JSON.parse(stored) : [];
};

export const storeQuestion = (question) => {
  const questions = getStoredQuestions();
  const newQuestion = {
    id: Date.now(),
    question: question,
    timestamp: new Date().toISOString(),
    status: 'pending'
  };
  questions.push(newQuestion);
  localStorage.setItem('fanhub_questions', JSON.stringify(questions));
  return newQuestion;
};